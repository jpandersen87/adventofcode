const CHUNK_SYMBOLS = [
    ["[","]"],
    ["{","}"],
    ["<",">"],
    ["(",")"]
];

enum CHUNK_SYMBOL_TYPE {
    OPEN,
    CLOSE,
    UNKNOWN
}

function getChunkSymbolType(a: string){
    for(let x of CHUNK_SYMBOLS){
        if(x[0] === a){
            return CHUNK_SYMBOL_TYPE.OPEN;
        } else if(x[1] === a){
            return CHUNK_SYMBOL_TYPE.CLOSE;
        }
    }

    return CHUNK_SYMBOL_TYPE.UNKNOWN
}

function getChunkSymbolOpposite(a: string){
    for(let x of CHUNK_SYMBOLS){
        if(x[0] === a){
            return x[1];
        } else if(x[1] === a){
            return x[0];
        }
    };

    return "?";
}

type LineReport = [string, string[], number, number];
type SyntaxReport = {
    incomplete: LineReport[],
    corrupted: LineReport[]
}

function createReport(a: string){
    const report = a.split("\n").reduce((syntaxReport, line, y) => {
        const stack: string[] = [];
        let incomplete: LineReport[] = [], corrupted: LineReport[] = [];

        line.split("").forEach((val, x) => {
            const symbolType = getChunkSymbolType(val);

            if(symbolType != CHUNK_SYMBOL_TYPE.UNKNOWN){
                const oppositeSymbol = getChunkSymbolOpposite(val);

                if(symbolType === CHUNK_SYMBOL_TYPE.CLOSE){
                    if(stack.length === 0 || stack[stack.length - 1] !== oppositeSymbol){
                        const report: LineReport = [val, [oppositeSymbol], x, y];
                        corrupted.push(report);
                    }
                    stack.pop();
                } else {
                    stack.push(val);
                }
            }

            if(x === line.length - 1 && stack.length > 0){
                if(corrupted.length === 0){
                    const missingCloseSymbols = stack.map(x => getChunkSymbolOpposite(x)).reverse();
                    const report: LineReport = [val, missingCloseSymbols, x, y];
                    incomplete.push(report);
                }
            }
        });

        syntaxReport.incomplete.push(...incomplete);
        syntaxReport.corrupted.push(...corrupted);

        return syntaxReport;
    }, {incomplete: [], corrupted: []} as SyntaxReport);

    return report;
}

